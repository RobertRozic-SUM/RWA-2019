        $this->validate(request(), [
            'title' => 'required|string|max:255',
            'price' => 'required|integer',
            'owner_id' => 'required|integer|max:255',
        ]);

        $car = Car::create(request([
            'title',
            'price',
            'owner_id'
        ]));

        return $car;
    }

